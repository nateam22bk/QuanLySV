    void themKhoaVien(){
        FileKhoaVien fileKhoaVien = new FileKhoaVien();
        String maVien = txtMaVien.getText().trim();
        String tenVien = txtTenVien.getText().trim();
        String soTCTN = txtSoTCTN.getText().trim();
        Integer soTCTNInt = 0;
        
        try {
            soTCTNInt = Integer.parseInt(soTCTN);
            if (maVien.length() < 2){
                throw new Exception();
            }
            if (tenVien.length() < 5){
                throw new Exception();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Dữ liệu nhập vào chưa đúng !");
            return;
        }
        
        ArrayList<KhoaVien> listKhoaVien = fileKhoaVien.docFileKhoaVien();
        KhoaVien khoaVien = new KhoaVien(maVien, tenVien, new ArrayList<SinhVien>(), new ArrayList<LopHoc>(), new ArrayList<>());
        khoaVien.SO_TC_TN = soTCTNInt;
        listKhoaVien.add(khoaVien);
        fileKhoaVien.ghiFileKhoaVien(listKhoaVien);
        JOptionPane.showMessageDialog(rootPane, "Thêm thành công !");
    }

